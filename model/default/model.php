function auto_save_time($table){
    foreach ($table->columns as $column){
        if($column->name == 'created_at' || $column->name == 'updated_at'){
            return true;
        }
    }
    return false;
}

function get_table_name($table){
    $data = [];
    foreach ($table->columns as $column){
        $data[] = $column->name;
    }
    return $data;
}



